#!/usr/bin/env python3
"""
Lexmark Subtitle Indexer
=========================

Downloads VTT subtitle files (EN + DE) for every Lexmark support video that
was scraped, converts the cue text to clean paragraphs, and stores them as
searchable chunks in krai_intelligence.chunks.

Flow per video
--------------
1. Read video rows that have metadata->tracks with srclang 'en' or 'de'
2. Download the VTT file via aiohttp
3. Parse VTT → clean text (strips timestamps / cue numbers)
4. Ensure a virtual "support video collection" document exists per model
   (krai_core.documents  document_type='support_video_collection')
5. Insert chunk(s) into krai_intelligence.chunks
6. Update krai_content.videos.related_chunks[] with the new chunk IDs
7. Embeddings are generated by the existing EmbeddingProcessor (separate run)

Usage
-----
    python scripts/index_lexmark_subtitles.py                  # all
    python scripts/index_lexmark_subtitles.py --limit 50       # first 50 videos
    python scripts/index_lexmark_subtitles.py --langs en       # English only
    python scripts/index_lexmark_subtitles.py --dry-run        # no DB writes
    python scripts/index_lexmark_subtitles.py --force          # re-index already done
"""

