async def load_videos_with_tracks(
    pool, schema_content: str, manufacturer_id: str, force: bool, limit: Optional[int], langs: set
) -> List[Dict]:
    """Return videos that have scraped tracks in the requested languages."""
    limit_clause = f"LIMIT {limit}" if limit else ""
    # Filter: has 'tracks' in metadata AND (force OR no subtitle chunk yet)
    force_clause = "" if force else "AND NOT (v.metadata ? 'subtitles_indexed')"
    query = f"""
        SELECT
            v.id,
            v.video_url,
            v.title,
            v.series_id,
            v.metadata AS metadata,
            p.model_number,
            p.id AS product_id
        FROM {schema_content}.videos v
        LEFT JOIN {schema_content}.video_products vp ON vp.video_id = v.id
        LEFT JOIN krai_core.products p ON p.id = vp.product_id
        WHERE v.manufacturer_id = $1::uuid
          AND v.metadata ? 'tracks'
          AND jsonb_array_length(v.metadata->'tracks') > 0
          {force_clause}
        ORDER BY v.created_at ASC
        {limit_clause}
    """
    async with pool.acquire() as conn:
        rows = await conn.fetch(query, manufacturer_id)

    results = []
    for row in rows:
        r = dict(row)
        metadata = r.get("metadata") or {}
        if isinstance(metadata, str):
            try:
                metadata = json.loads(metadata)
            except Exception:
                metadata = {}
        tracks = metadata.get("tracks", [])
        # Keep only tracks in requested languages
        filtered = [t for t in tracks if normalize_lang(t.get("srclang", "")) in langs]
        if filtered:
            r["tracks_filtered"] = filtered
            results.append(r)
    return results


async def get_or_create_virtual_document(
    pool, model_number: str, manufacturer_id: str, dry_run: bool
) -> str:
    """
    Return the document_id for the virtual 'support video collection' document
    of a given model. Creates it if it doesn't exist.
    """
    filename = f"lexmark_{model_number}_support_videos.virtual"

    async with pool.acquire() as conn:
        existing = await conn.fetchval(
            "SELECT id FROM krai_core.documents WHERE filename = $1 LIMIT 1",
            filename,
        )
        if existing:
            return str(existing)

        if dry_run:
            return f"dry-run-doc-{model_number}"

        doc_id = await conn.fetchval(
            """
            INSERT INTO krai_core.documents
                (filename, document_type, language, processing_status,
                 manufacturer, manufacturer_id, extracted_metadata)
            VALUES ($1, 'support_video_collection', 'multi', 'completed',
                    'Lexmark', $2::uuid, $3::jsonb)
            RETURNING id
            """,
            filename,
            manufacturer_id,
            json.dumps({"model_number": model_number, "source": "lexmark_support_videos"}),
        )
        logger.debug("Created virtual document for %s: %s", model_number, doc_id)
        return str(doc_id)


async def upsert_chunk(
    pool,
    schema_intel: str,
    document_id: str,
    chunk_index: int,
    text: str,
    language: str,
    video_id: str,
    video_url: str,
    model_number: str,
    dry_run: bool,
) -> Optional[str]:
    """Insert or update a subtitle chunk. Returns chunk_id."""
    fingerprint = hashlib.sha256(f"{document_id}:{language}:{text}".encode()).hexdigest()[:32]

    if dry_run:
        return f"dry-run-chunk-{fingerprint[:8]}"

    async with pool.acquire() as conn:
        chunk_id = await conn.fetchval(
            f"""
            INSERT INTO {schema_intel}.chunks
                (document_id, text_chunk, chunk_index, fingerprint,
                 processing_status, metadata)
            VALUES ($1::uuid, $2, $3, $4, 'pending', $5::jsonb)
            ON CONFLICT (fingerprint) DO NOTHING
            RETURNING id
            """,
            document_id,
            text,
            chunk_index,
            fingerprint,
            json.dumps({
                "chunk_type":   "video_subtitle",
                "language":     language,
                "video_id":     video_id,
                "video_url":    video_url,
                "model_number": model_number,
                "source":       "lexmark_support",
            }),
        )
        if chunk_id:
            return str(chunk_id)
        # Already exists — return existing id
        existing = await conn.fetchval(
            f"SELECT id FROM {schema_intel}.chunks WHERE fingerprint = $1 LIMIT 1",
            fingerprint,
        )
        return str(existing) if existing else None


async def update_video_chunks(
    pool, schema_content: str, video_id: str, chunk_ids: List[str], dry_run: bool
) -> None:
    """Append chunk_ids to videos.related_chunks and mark subtitles_indexed."""
    if dry_run or not chunk_ids:
        return

    async with pool.acquire() as conn:
        # Merge into existing related_chunks array (avoid duplicates)
        await conn.execute(
            f"""
            UPDATE {schema_content}.videos
            SET
                related_chunks = (
                    SELECT ARRAY(
                        SELECT DISTINCT unnest(
                            COALESCE(related_chunks, ARRAY[]::uuid[]) ||
                            $2::uuid[]
                        )
                    )
                ),
                metadata = COALESCE(metadata, '{{}}'::jsonb)
                           || jsonb_build_object('subtitles_indexed', true),
                updated_at = NOW()
            WHERE id = $1::uuid
            """,
            video_id,
            chunk_ids,
        )


# ---------------------------------------------------------------------------
# Download + index one video
# ---------------------------------------------------------------------------

async def process_video(
    session:    aiohttp.ClientSession,
    semaphore:  asyncio.Semaphore,
    pool,
    schema_content: str,
    schema_intel:   str,
    video:          Dict,
    dry_run:        bool,
) -> Dict:
    video_id     = str(video["id"])
    video_url    = video.get("video_url", "")
    model_number = video.get("model_number") or "Unknown"
    tracks       = video.get("tracks_filtered", [])

    result = {"video_id": video_id, "chunks_created": 0, "errors": []}

    # Get or create virtual document for this model
    doc_id = await get_or_create_virtual_document(pool, model_number, LEXMARK_ID, dry_run)

    chunk_ids: List[str] = []
    chunk_index = 0

    async with semaphore:
        for track in tracks:
            lang = normalize_lang(track.get("srclang", ""))
            vtt_url = track.get("src", "")
            if not vtt_url:
                continue

            try:
                async with session.get(vtt_url, timeout=aiohttp.ClientTimeout(total=15)) as resp:
                    if resp.status != 200:
                        result["errors"].append(f"HTTP {resp.status} for {vtt_url}")
                        continue
                    vtt_text = await resp.text(encoding="utf-8", errors="replace")

                clean_text = parse_vtt(vtt_text)
                if not clean_text or len(clean_text) < 10:
                    result["errors"].append(f"Empty VTT for {lang}: {vtt_url}")
                    continue

                chunk_id = await upsert_chunk(
                    pool, schema_intel,
                    document_id=doc_id,
                    chunk_index=chunk_index,
                    text=clean_text,
                    language=lang,
                    video_id=video_id,
                    video_url=video_url,
                    model_number=model_number,
                    dry_run=dry_run,
                )
                if chunk_id:
                    chunk_ids.append(chunk_id)
                    result["chunks_created"] += 1
                    chunk_index += 1

            except asyncio.TimeoutError:
                result["errors"].append(f"Timeout: {vtt_url}")
            except Exception as exc:
                import traceback
                result["errors"].append(f"Error {lang}: {exc} | {traceback.format_exc().splitlines()[-3]}")

    if chunk_ids:
        await update_video_chunks(pool, schema_content, video_id, chunk_ids, dry_run)

    return result


